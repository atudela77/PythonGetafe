# Es un programa que desamos ejecutar, es decir,
#  los métodos se declara al inicio.
# La sintaxis de nombres de métodos es primera palabra
# en minúsucula y el resto con la primera en mayúscula.
def primerMetodo():
    # Este código nunca se ejecutará sino lo llamamos de
    #  forma explícita.
    print("Soy el primer método")


def segundoMetodo():
    # Este código nunca se ejecutará sino lo llamamos de
    #  forma explícita.
    print("Soy el segundo método")


# Aquí tenemos el código principal.
# Lo que lo diferencia de formar parte del método es la
#  tabulación, el indentado.
print("Ejemplo de métodos")
# Podemos llamar al método si lo deseamos
primerMetodo()
segundoMetodo()
primerMetodo()
segundoMetodo()
primerMetodo()
print("Fin del programa")
